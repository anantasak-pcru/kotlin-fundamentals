/**
 * Priorites is (), *, /, +, -
 */
fun main(args: Array<String>) {

    var numberInt = 5
    var numberFloat = 10.5

    println(numberInt + numberFloat)
    println(numberInt - numberFloat)
    println(numberInt * numberFloat)
    println(numberInt / numberFloat)


    /**
     * Increment and Decrement
     */
    var sampleValue = 5
    println("sampleValue: $sampleValue")
    println("++sampleValue: ${++sampleValue}")
    println("sampleValue++: ${sampleValue++}")
    println("sampleValue: $sampleValue")
}