fun main(args: Array<String>) {

    val numberInt = 555
    println("numberInt: $numberInt")

    val numberString = numberInt.toString()
    println("numberString: $numberString")

    val numberDouble = numberInt.toDouble()
    println("numberDouble: $numberDouble")

    val numberFloat = numberInt.toFloat()
    println("numberFloat: $numberFloat")
}